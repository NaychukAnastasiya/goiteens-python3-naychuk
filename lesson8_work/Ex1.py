def functionForTwoNumbers(first_num,second_num):
    sum_of_numbers = first_num + second_num
    divide_on_hundred = sum_of_numbers / 100
    result = divide_on_hundred * 25
    print(result)

# викликаємо фукнцію, написавши її назву, та передавши в дужки числа
functionForTwoNumbers(2,3)
functionForTwoNumbers(5,6)
functionForTwoNumbers(3,6)
# Створити цикл for від 1 до 100 , надрукувати на екран 
# кількість чисел які діляться на 2 . Створити змінну count = 0 
# перед циклом for
#  для того щоб зберігати в ній кількість чисел які діляться на 2.

count = 0
for i in range(1,100,1):
    if (i%2 ==0):
        count = count +1
print("Кількість чисел які діляться на 2: ",count)
